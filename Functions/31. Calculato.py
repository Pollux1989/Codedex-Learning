print("#####--------31. Calculator--------#####")

#Sum
def add(a, b):
  answer = a + b
  return answer
print(add(10, 50))          

#Substract
def substract(a,b):
  answer = a - b
  return answer
print(substract(20,15))

#Multiply
def multiply(a,b):
  answer = a * b
  return answer
print(multiply(30,50))

#Divide
def divide(a,b):
  answer = a / b
  return answer
print(divide(10,1))

#Exp
def exp(a,b):
  answer = a ** b
  return answer
print(exp(50,8))
